template <class T>
inline bool chmax(T &a, T b)
{
    if (a < b)
    {
        a = b;
        return true;
    }
    return false;
}
template <class T>
inline bool chmin(T &a, T b)
{
    if (a > b)
    {
        a = b;
        return true;
    }
    return false;
}
typedef long long ll;
typedef pair<ll, ll> P;
const int INF = 1e9 + 7;
const ll LINF = 1LL << 60;

int a[10010];

int main()
{
    ios_base::sync_with_stdio(false);
    cin.tie(NULL);
    cout.tie(NULL);

    int n;
    cin >> n;
    for (int i = 0; i < n; i++)
        cin >> a[i];

    int ans = 0;

    for (int l = 0; l < n; l++)
    {
        int x = a[l];
        for (int r = l; r < n; r++)
        {
            x = min(x, a[r]);
            ans = max(ans, x * (r - l + 1));
        }
    }

    cout << ans << endl;

    return 0;
}